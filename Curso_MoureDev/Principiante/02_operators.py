### Operators ###

### Operadores Aritmeticos ###

print(3+4)
print(3-4)
print(3*4)
print(3/4)
print(3**4)
print(5 % 4)
# División en la que el resultado se aproxima un numero, eliminando la parte decimal
print(9 // 4)
print("Hola"*3)

### Operadores Comparativo ###

print(3 > 4)
print(3 < 4)
print(3 <= 4)
print(3 >= 4)
print(3 == 4)
print(3 != 4)

print("Hola" > "Python")  # Ordenación alfabética por ASCII
print("Hola" < "Python")
print("Hola" > "Python")
print("Hola" > "Python")

### Operadores Lógicos ###

